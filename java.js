var paciente = document.querySelector('#primeiro-paciente')
console.log(paciente)

var valorHTML = paciente.querySelector('.info-HTML')
var HTML = parseInt(valorHTML.textContent)
console.log(HTML)

var valorCSc = paciente.querySelector('.info-CSc')
var CSc = parseInt(valorCSc.textContent)
console.log(CSc)

var valorJavaScript = paciente.querySelector('.info-JavaScript')
var JavaScript = parseInt(valorJavaScript.textContent)
console.log(JavaScript)

var valorMedia = (HTML + CSc + JavaScript) / 3
console.log(valorMedia)

var substituirMedia = paciente.querySelector('.info-Media')
substituirMedia.textContent = valorMedia.toFixed(2)

